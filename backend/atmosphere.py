def get_weather(day: int) -> dict:
    """根据天数获取天气"""
    # 前5天天气较好，之后逐渐恶化
    if day <= 5:
        weights = [0.6, 0.1, 0.1, 0.1, 0.1]
    elif day <= 15:
        weights = [0.3, 0.2, 0.2, 0.2, 0.1]
    else:
        weights = [0.15, 0.25, 0.25, 0.25, 0.1]
    
    weather_id = random.choices(list(WEATHER_TYPES.keys()), weights=weights, k=1)[0]
    return WEATHER_TYPES[weather_id]


