def get_location_atmosphere(location_id: str, day: int) -> str:
    """获取地点的动态氛围描写"""
    atmosphere = LOCATION_ATMOSPHERE.get(location_id, {})
    if not atmosphere:
        return ""
    
    # 根据天数选择阶段
    if day <= 10:
        pool = atmosphere.get("early", [])
    elif day <= 20:
        pool = atmosphere.get("mid", [])
    else:
        pool = atmosphere.get("late", [])
    
    if pool:
        return random.choice(pool)
    return ""


def get_inspect_description(location_id: str, target: str) -> str:
    """获取检查特定物品的描述"""
    atmosphere = LOCATION_ATMOSPHERE.get(location_id, {})
    inspect = atmosphere.get("inspect", {})
    return inspect.get(target, "")


